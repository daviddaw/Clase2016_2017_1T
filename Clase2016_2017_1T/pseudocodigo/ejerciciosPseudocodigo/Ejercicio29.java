package ejerciciosPseudocodigo;

public class Ejercicio29 {

	public static void main(String[] args) {
		// tablasde multiplicar del 1 al 10
		
		for (int i = 0; i <= 10; i++) {
			System.out.println("");
			System.out.println("tabla del "+i);
			for (int j = 0; j <= 10; j++) {
				System.out.println(i+" por "+j+" = "+i*j);
				
			}
			
			
		}

	}

}
